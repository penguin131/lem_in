#include "libft.h"

void	ft_putnbr(int n)
{
	long int num;

	num = (long int)n;
	if (num < 0)
	{
		num *= -1;
		ft_putchar('-');
	}
	if (num < 10)
		ft_putchar(num + '0');
	else
	{
		ft_putnbr(num / 10);
		ft_putnbr(num % 10);
	}
}
