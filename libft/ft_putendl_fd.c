#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	if (fd != -1 && s)
	{
		while (*s != '\0')
			ft_putchar_fd(*s++, fd);
		ft_putchar_fd('\n', fd);
	}
}
